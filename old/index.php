		<p>People have said lots of good things about Cloudmouth. Here are a few that were spoken with keyboards:</p>
		<p>
			“Kyle’s songcraft skips the mold completely. His songs get into your brain precisely because they are not familiar&nbsp;…&nbsp;complimented by the obvious musical chemistry between him and percussionist-extordinaire Amoretta Layne. Their apparent disregard for genre suits the music beautifully as they flow effortlessly from major key melodic perfection one moment, to flat out lysergic disabandon the next.” – Ben Lowry, <a href="http://bangokbang.net/" target="_blank">Bang Ok Bang</a>
		</p>
		<p>
			"Numann’s folky, sentimental croon went down easy and eased our minds ... drummer Amoretta Taylor’s fluid, intuitive percussion that really sold this routine. Using a variety of sticks, mallets, bongos and shakers to be as quiet or loud as needed, Taylor ebbed, flowed and cascaded with Numann’s occasionally noisy interludes and punched up his woeful delivery with an invigorating backbeat." - <a href="http://www.nashvillescene.com/music/article/13060568/thelma-and-the-sleaze-leads-the-charge-at-bitchfest-5" target="_blank">Nashville Scene</a>
