				<h4>Drunk On What I Am</h4>
				<p>Recorded in sessions from 2010-2014, this album is a mix of composed song forms and experimental sound combination projects. The songs were birthed out of personal introspection, heartache, toothache, doubt and hunger and also strength, virtue, struggle and resolution. This recording features a small cast of friends and a large cast of formal and improvisational instruments including (but not limited to) Guitar, Accordion, Bass, Trumpet, Trombone, Ukulele, Banjo, Mandolin, Piano, Drums, Whiskey Barrel, Trash Can, Suitcase, Handlebars, Oven, Cabinets, Chains, &amp; Lumber.</p>
				<p>All recording &amp; engineering and most playing by Kyle Numann.</p>

				<p>Album is available as a digital download, with printable album artwork .pdf.</p>

